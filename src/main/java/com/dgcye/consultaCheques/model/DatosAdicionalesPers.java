package com.dgcye.consultaCheques.model;

public class DatosAdicionalesPers {
	private String nombre;
	private String docu;
	private String descripcion;
	private String tipoOrg;
	private String reg;
	private String prov;
	private Integer numero;
	private Integer distrito;
	private Integer dependencia;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDocu() {
		return docu;
	}
	public void setDocu(String docu) {
		this.docu = docu;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getTipoOrg() {
		return tipoOrg;
	}
	public void setTipoOrg(String tipoOrg) {
		this.tipoOrg = tipoOrg;
	}
	public String getReg() {
		return reg;
	}
	public void setReg(String reg) {
		this.reg = reg;
	}
	public String getProv() {
		return prov;
	}
	public void setProv(String prov) {
		this.prov = prov;
	}
	public Integer getNumero() {
		return numero;
	}
	public void setNumero(Integer numero) {
		this.numero = numero;
	}
	public Integer getDistrito() {
		return distrito;
	}
	public void setDistrito(Integer distrito) {
		this.distrito = distrito;
	}
	public Integer getDependencia() {
		return dependencia;
	}
	public void setDependencia(Integer dependencia) {
		this.dependencia = dependencia;
	}
	
	
	
	

}
